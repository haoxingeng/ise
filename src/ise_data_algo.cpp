//-----------------------------------------------------------------------------

BYTE calcCrc8(PVOID data, int dataSize)
{
    char *p = (char*)data;
    BYTE result = 0;

    while (dataSize--)
    {
        result ^= *p++;
        for (int i = 0; i < 8; ++i)
        {
            if (result & 0x01)
                result = (result >> 1) ^ 0x8C;
            else
                result >>= 1;
        }
    }

    return result;
}

