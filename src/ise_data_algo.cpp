binary Format_HEX::doEncode(PVOID data, int size)
{
    binary result;
    const char *t = charTable();
    unsigned char *s;
    char *d;

    if (size <= 0)
        return result;

    result.resize(size * 2);
    d = (char*)result.c_str();
    s = (unsigned char*)data;

    while (size > 0)
    {
        d[0] = t[*s >> 4];
        d[1] = t[*s & 0x0F];
        d += 2;
        s++;
        size--;
    }

    return result;
}

//-----------------------------------------------------------------------------

binary Format_HEX::doDecode(PVOID data, int size)
{
    binary result;
    const char *t = charTable();
    char *s;
    unsigned char *d;
    int i, p;
    bool hasIdent;

    if (size <= 0)
        return result;

    result.resize(size / 2 + 1);
    d = (unsigned char*)result.c_str();
    s = (char*)data;
    i = 0;
    hasIdent = false;

    while (size > 0)
    {
        p = tableFind(*s, t, 18);
        if (p < 0)
            p = tableFind(toupper(*s), t, 16);
        if (p < 0)
            iseThrowDataAlgoException(formatString(S_INVALID_STRING_FORMAT, "Format_HEX").c_str());

        s++;
        if (p >= 0)
        {
            if (p > 16)
            {
                if (!hasIdent)
                {
                    hasIdent = true;
                    i = 0;
                    d = (unsigned char*)result.c_str();
                }
            }
            else
            {
                if (i % 2 != 0)
                {
                    *d = *d | p;
                    d++;
                }
                else
                    *d = p << 4;
                i++;
            }
        }
        size--;
    }

    result.resize((char*)d - (char*)result.c_str());
    return result;
}

//-----------------------------------------------------------------------------

bool Format_HEX::doIsValid(PVOID data, int size)
{
    bool result = true;
    const char *t = charTable();
    int l = (int)strlen(t);
    char *s = (char*)data;

    while (result && (size > 0))
    {
        if (tableFind(*s, t, l) >= 0)
        {
            size--;
            s++;
        }
        else
            result = false;
    }

    return result;
}

