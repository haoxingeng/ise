///////////////////////////////////////////////////////////////////////////////
// Format_MIME64

binary Format_MIME64::doEncode(PVOID data, int size)
{
    binary result;
    const char *t = charTable();
    char *d;
    unsigned char *s;
    DWORD b;
    int i;

    if (size <= 0)
        return result;

    result.resize(size * 4 / 3 + 4);
    d = (char*)result.c_str();
    s = (unsigned char*)data;

    while (size >= 3)
    {
        size -= 3;
        b = (s[0] << 16) | (s[1] << 8) | s[2];
        d[0] = t[ b >> 18 & 0x3F ];
        d[1] = t[ b >> 12 & 0x3F ];
        d[2] = t[ b >>  6 & 0x3F ];
        d[3] = t[ b       & 0x3F ];
        d += 4;
        s = &s[3];
    }

    while (size > 0)
    {
        b = 0;
        for (i = 0; i <= 2; i++)
        {
            b = b << 8;
            if (size > 0)
            {
                b |= s[0];
                s = &s[1];
            }
            size--;
        }
        for (i = 3; i >= 0; i--)
        {
            if (size < 0)
            {
                d[i] = t[64];
                size++;
            }
            else
                d[i] = t[ b & 0x3F ];
            b = b >> 6;
        }
        d += 4;
    }

    result.resize((char*)d - (char*)result.c_str());
    return result;
}

//-----------------------------------------------------------------------------

binary Format_MIME64::doDecode(PVOID data, int size)
{
    binary result;
    const char *t = charTable();
    char *s, *d, *l;
    int i, j;
    DWORD b;

    if (size <= 0)
        return result;

    result.assign((char*)data, size);
    d = (char*)result.c_str();
    s = d;
    l = s + size;
    j = 0;

    while (s < l)
    {
        b = 0;
        j = 4;
        while (j > 0 && s < l)
        {
            i = tableFind(*s, t, 65);
            s++;
            if (i >= 0)
            {
                if (i < 64)
                {
                    b = b << 6 | (BYTE)i;
                    j--;
                }
                else
                    l = s;
            }
        }

        if (j > 0)
        {
            if (j >= 4)
            {
                j = 0;
                break;
            }
            else
                b = b << (6 * j);
        }
        i = 2;
        while (i >= 0)
        {
            d[i] = (char)b;
            b = b >> 8;
            i--;
        }
        d += 3;
    }

    result.resize((char*)d - (char*)result.c_str() - j);
    return result;
}

