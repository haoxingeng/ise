//-----------------------------------------------------------------------------

void Cipher_DES::DoInitKey(PBYTE data, PDWORD key, bool reverse)
{
    const BYTE ROT[16] = {1,2,4,6,8,10,12,14,15,17,19,21,23,25,27,28};

    DWORD i, j,l, m, n;
    BYTE pcm[56], pcr[56];
    DWORD k[32];

    memset(k, 0, sizeof(k));
    for (i = 0; i < 56; i++)
    {
        if ((data[DES_PC1[i] >> 3] & (0x80 >> (DES_PC1[i] & 0x07))) != 0)
            pcm[i] = 1;
        else
            pcm[i] = 0;
    }
    for (i = 0; i < 16; i++)
    {
        if (reverse)
            m = (15 - i) << 1;
        else
            m = i << 1;
        n = m + 1;
        for (j = 0; j < 28; j++)
        {
            l = j + ROT[i];
            if (l < 28)
                pcr[j] = pcm[l];
            else
                pcr[j] = pcm[l - 28];
        }
        for (j = 28; j < 56; j++)
        {
            l = j + ROT[i];
            if (l < 56)
                pcr[j] = pcm[l];
            else
                pcr[j] = pcm[l - 28];
        }
        l = 0x1000000;
        for (j = 0; j < 24; j++)
        {
            l = l >> 1;
            if (pcr[DES_PC2[j]] != 0)
                k[m] |= l;
            if (pcr[DES_PC2[j+24]] != 0)
                k[n] |= l;
        }
    }
    for (i = 0; i < 16; i++)
    {
        m = i << 1;
        n = m + 1;
        key[0] =
            (k[m] & 0x00FC0000) <<  6 |
            (k[m] & 0x00000FC0) << 10 |
            (k[n] & 0x00FC0000) >> 10 |
            (k[n] & 0x00000FC0) >>  6;
        key[1] =
            (k[m] & 0x0003F000) << 12 |
            (k[m] & 0x0000003F) << 16 |
            (k[n] & 0x0003F000) >>  4 |
            (k[n] & 0x0000003F);
        key = &key[2];
    }

    protectBuffer(k, sizeof(k));
    protectBuffer(pcm, sizeof(pcm));
    protectBuffer(pcr, sizeof(pcr));
}

